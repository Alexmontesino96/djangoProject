from django.contrib import admin
from django.urls import path
from drf_yasg import openapi
from drf_yasg.views import get_schema_view
from rest_framework import permissions
from user.api.router import urlpatterns as user_urlpatterns
from django.urls import include
from categories.api.routers import router as categories_router
from post.api.router import router as post_router
from comments.api.router import router as comments_router

schema_view = get_schema_view(
   openapi.Info(
      title="Snippets API",
      default_version='v1',
      description="Test description",
      terms_of_service="https://www.google.com/policies/terms/",
      contact=openapi.Contact(email="contact@snippets.local"),
      license=openapi.License(name="BSD License"),
   ),
   public=True,
   permission_classes=(permissions.AllowAny,),
)

urlpatterns = [
    path("admin/", admin.site.urls),
    path('docs/', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    path('redoc/', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),
    path('api/auth/', include(user_urlpatterns), name='auth'),
    path('api/', include(categories_router.urls)),
    path('api/', include(post_router.urls)),
    path('api/', include(comments_router.urls)),

]
